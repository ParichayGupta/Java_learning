package com.learning.constructor;

public class ParameterisedConstructor {

    String test1;
    int test2;

    /*
     * Parameterised constructor
     *
     * - parameters should be passed in sequential manner
     * and when calling the constructor we need to pass the same sequence of values.
     * */
    ParameterisedConstructor(int test2, String test1) {

    }

    ParameterisedConstructor(int test2){

    }

    ParameterisedConstructor(String test1){

    }

    ParameterisedConstructor(){

    }
}
