package com.learning.staticKeyword;

public class Student {

    String name;
    int age;
    int batch;
    String schoolName;

    public Student() {
    }

    public Student(String name, int age, int batch, String schoolName) {
        this.name = name;
        this.age = age;
        this.batch = batch;
        this.schoolName = schoolName;
    }
}
