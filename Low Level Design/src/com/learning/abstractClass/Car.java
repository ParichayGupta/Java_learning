public abstract class Car {

    abstract protected String engineType(String engine);

     int noOfWindows(int noOfWindows){
        return noOfWindows;
     }

     int noOfSeats(int noOfSeats){
         return noOfSeats;
     }

     int noOfWheels(int noOfWheels){
         return noOfWheels;
     }
}
